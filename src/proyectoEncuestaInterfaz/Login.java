    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        String username = inputUsuario.getText();
        String password = inputPassword.getText();
        System.out.println(controlador.validarUsuario(username, password));
        boolean encontrado = controlador.validarUsuario(username, password);
        if (encontrado) {
            Cuenta cuenta = new Cuenta();
            cuenta.setVisible(true);
            cuenta.setControl(this.getControlador());
            cuenta.llenarDatosCuenta();
            this.dispose();
        }else{
            success.setText("Usuario: " + username + " no existe");
        }
    }//GEN-LAST:event_btnLoginActionPerformed

